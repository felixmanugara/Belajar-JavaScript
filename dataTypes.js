// Tipe data Number
/* JavaScript hanya mendukung satu tipe data number yaitu
   berupa bilangan bulat atau desimal, pada bahasa pemrograman 
   lain biasanya tipe data number dibedakan antara bilangan bulat
   dan bilangan desimal, tetapi pada JavaScript semuanya disatukan */

console.log(100); // bilangan bulat
console.log(100.124); // bilangan desimal / pecahan

//Number Notation
/* JavaScript mendukung Number Notation yang secara default mengunakan 
   basis 10, JavaScript juga mendukung binary, octal, dan hexadecimal */

console.log(0xEF); // hexadecimal diawali dengan 0x
console.log(0b00001111); // binary diawali dengan 0b
console.log(0o17); // octal diawal dengan 0o

// Tipe Data Boolean
/* merupakan tipe data yang memiliki nilai True atau False, yang mana
   benar direpresentasikan oleh True dan salah di representasikan oleh False */

console.log(true);
console.log(false);

// Tipe data String
/* tipe data yang merupakan kumpulan karakter, yang dapat ditulis menggunakan 
   "" atau '' */

console.log("Felix");
console.log('Marco');

/* kita juga bisa menggabungkan string dengan menggunakan operator + (concatenate) */

console.log("Felix " + "Marco");

