/* typeof operator 
   typeof merupakan sebuah operator yang digunakan untuk melihat
   tipe data dari sebuah variable atau value.
   karena JavaScript merupakan dynamic languange, maka kita kadang
   perlu untuk mengecek tipe data sebuah value atau variable.
   hasil dari operator typeof adalah string tipe datanya
*/

let data = "Felix";

const dataType = typeof data;
console.info(`tipe data adalah: ${dataType}`)