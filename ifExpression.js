/* If Expression
   Dalam JavaScript, if adalah salah satu kunci yang digunakan untuk
   percabangan.
   Percabangan artinya kita dapat mengeksekusi kode program tertentu
   ketika suatu kondisi terpenuhi.
   Hampir semua bahasa pemrograman mendukung if expression.
*/
const examGrade = 80 ;

if (examGrade > 75) {
    console.info("You Passed the Exam!!");
} else if (examGrade > 60) {
    console.info("Not Bad!!")
} else {
    console.info("Try Again Next Semester!!")
}
